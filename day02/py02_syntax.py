# 변수 - 주석

상자 = 'Apple' # 문자/문자열열
print(상자)

상자 = '파인애플' # 파이썬에서 문자열은 ' " 동일
print(상자)

상자 = True # 참/거짓
print(상자)

상자 = 10_000_000 # 정수
print(상자)

box = 3.1415926535 # 실수
print(box)

box = [1,2,3,4,5] # 리스트(배열)
print(box)

box = (1,2,3,4,5) # 튜플
print(box)

box = [1, '2', True, [3,6,9], (11,22)] # 혼합
print(box)



